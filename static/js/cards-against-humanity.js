function getCardsAgainstHumanity() {
  const now = new Date();
  const date = ((now.getFullYear() * 297)
	  + ((now.getMonth() + 1) * 22) 
	  * (now.getDate() * 887 + 2)) % blackCards.length;

  let blackCard = blackCards[date];
  let whiteCardList = [];
  let count = (blackCard.match(/____/g) || []).length;
  let trueCount = count;
  count === 0 ? count = 1 : count = count;
  
  for(let i = 0; i < count; i++) {
    // i can't be asked about duplicates, whatever bro
    let wh = (whiteCards[Math.floor(Math.random() * whiteCards.length)]);
    whiteCardList.push(wh.slice(0, -1));
  }

  // format underlines
  // if 0, '[black] [white]'
  // if more than 0, replace all ____ with <u>[white]</u> with each white card
  
  if(trueCount === 0) {
    return `${ blackCard } <b>${ whiteCardList[0] }</b>`;
  }

  for(let i = 0; i < trueCount; i++) {
    blackCard = blackCard.replace('____', `<u>${ whiteCardList[i] }</u>`);
  }

  return blackCard;
}
